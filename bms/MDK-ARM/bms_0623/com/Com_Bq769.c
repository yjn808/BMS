/**
 * @brief 根据数值查询表的坐标
 *
 * 1  设定本次查询的起点   用上次的查询索引 作为本次查询的起点
 * 2  循环查询  范围 >0  <表格总长度
 * 3     循环过程  判断是否命中   命中 大于等于当前坐标对应的值 小于坐标+1的值   返回坐标前 要修改last_index 和 last_value
 * 4                未命中  如果上次值大于本次值  坐标--
 *                          反之   坐标++
 * 5   极值处理 如果值不在表中范围  比最小值小  给最小值  比最大值大 给最大值
 *
 */
uint16_t
getTableIndexByValue(int value, int *table, uint16_t table_size, uint16_t *last_index, int *last_value)
{

    // * 1  设定本次查询的起点   用上次的查询索引 作为本次查询的起点
    int16_t cur_index = *last_index;

    //  * 2  循环查询  范围 >0  <表格总长度
    while (cur_index < table_size && cur_index >= 0)
    {
        //  * 3     循环过程  判断是否命中   命中 大于等于当前坐标对应的值 小于坐标+1的值   返回坐标前 要修改last_index 和 last_value
        if (value >= table[cur_index] && value < table[cur_index + 1])
        {
            *last_index = cur_index;
            *last_value = table[cur_index];
            return cur_index;
        }
        //  * 4                未命中  如果上次值大于本次值  坐标--
        //  *                          反之   坐标++
        else if (*last_value > value)
        {
            cur_index--;
        }
        else if (*last_value < value)
        {
            cur_index++;
        }
    }
    if (cur_index < 0)
    {
        return 0;
    }
    else if (cur_index >= table_size)
    {
        return table_size - 1;
    }

    return cur_index;
}

static uint16_t last_index_tempr = 125 - 25; // 25℃的坐标
static int last_value_tempr = 10000;         // 25℃的电阻

/**
 * @brief 通过查表法把电阻换算为温度
 */
int8_t Com_BQ769_getTemperByResist(uint32_t resist_value)
{

    uint16_t index = getTableIndexByValue((int)resist_value, resist_temper_table, 181, &last_index_tempr, &last_value_tempr);

    return 125 - index; // 温度
}

static uint16_t last_index_soc = 50; // 50%电量
static int last_value_soc = 3679;    // 50%电量的电压

/**
 * @brief 通过查表法把电压换算为电量百分比
 */
uint8_t Com_BQ769_getPercentByVoltage(uint16_t voltage_value)
{
    uint16_t index = getTableIndexByValue((int)voltage_value, voltage_soc_table, 101, &last_index_soc, &last_value_soc);

    return index; // 索引和电量百分比值一致
}
